CREATE TABLE Users (
    UserId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name NVARCHAR2(100) NOT NULL,
    Email NVARCHAR2(100) NOT NULL UNIQUE,
    Password NVARCHAR2(100) NOT NULL,
    CreatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE Categories (
    CategoryId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name NVARCHAR2(100) NOT NULL
);

CREATE TABLE Transactions (
    TransactionId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    UserId NUMBER NOT NULL,
    CategoryId NUMBER NOT NULL,
    Amount NUMBER(10, 2) NOT NULL,
    TransactionDate DATE DEFAULT SYSDATE,
    FOREIGN KEY (UserId) REFERENCES Users(UserId),
    FOREIGN KEY (CategoryId) REFERENCES Categories(CategoryId)
);
